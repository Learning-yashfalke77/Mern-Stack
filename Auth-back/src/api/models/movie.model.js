const mongoose = require('mongoose');
const { Schema } = mongoose

const movieSchema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true,
        minlength: 5,
        maxlength: 255
    },
    genre: {
        type: Schema.Types.ObjectId,
        ref: "Genre"
    },
    numberInStock: {
        type: Number,
        required: true,
        min: 0,
        max: 255
    },
    dailyRentalRate: {
        type: Number,
        required: true,
        min: 0,
        max: 255
    }
}, { timestamps: true })

module.exports = mongoose.model('Movie', movieSchema)